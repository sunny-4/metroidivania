using UnityEngine;

public class GameManager : MonoBehaviour
{
    public static bool HasDoubleJump = false;

    private static GameManager instance;

    private void Awake()
    {
        if (instance == null)
        {
            instance = this;
            DontDestroyOnLoad(gameObject);
        }
        else
        {
            Destroy(gameObject);
        }
    }
}
